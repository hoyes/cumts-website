<p>Cambridge University Musical Theatre Society exists to enable high quality musical theatre to be staged in Cambridge and to allow its members opportunities to hone their skills, be it as a performer, musician, techie or production team member.</p>
<hr />
<h2>Bereavement: The Musical at the Edinburgh Fringe</h2>
<p>
After its phenomenal premiere at the ADC Theatre in February, the CUMTS' exciting new musical can be seen at C Venues in Edinburgh this summer.</p>
<p>Bereavement is a song and dance. Watch people singing and dancing about it.
In this bitter-sweet new musical, the big secret about bereavement is revealed: no-one really knows what they should be doing. Curtain up and spotlight on six characters as they blunder through bereavement's 
funny little cabaret, trying to make sense of grief and the unexpected extras it throws up. But once 
you've survived the barrage of tasteless funerals; needless guilt; 'helpful' advice and useless cliches, is anyone any the wiser about what it really means to lose someone?</p>
<p>The show is being presented by CUMTS in association with Pembroke Players and CUADC. Tickets are 
available from the <a href="http://www.edfringe.com/whats-on/musicals-and-operas/bereavement-the-musical">Fringe website</a>.</p>
